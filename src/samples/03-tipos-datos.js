/* Tipos de Datos */

/* 1. Primitivos */
const stringVar = "Hola mundo"; // string
const numberVar = 30; // number
const booleanVar = true; // boolean
const nullVar = null; // null
const undefinedVar = undefined; // undefined

/* 2. Compuestos */
const objectVar = {}; // object
const functionVar = () => {}; // function

/* 3. Casting */
const manualCasting = Number("1"); // 1
const automaticCasting = "5" + 5; // "55"

/* 4. Template literals */
const templateLiteral = `Hola ${1 + 1}`; // "Hola 2"
